    // --- Helper Functions ---
    // Function to render a list of questions within the modal
    function renderQuestionListInModal(questions, title) {
        modalListContainer.innerHTML = ''; // Clear previous content

        const titleHeader = document.createElement('h2');
        titleHeader.textContent = title;
        modalListContainer.appendChild(titleHeader);

        // Group questions by category if provided
        for (const category in questions) {
            const categoryHeader = document.createElement('h3');
            categoryHeader.textContent = category;
            modalListContainer.appendChild(categoryHeader);

            questions[category].forEach(qa => {
                const questionItem = document.createElement('div');
                questionItem.className = 'question-item';
                questionItem.innerHTML = qa.q;

                const answerText = document.createElement('div');
                answerText.className = 'answer-text';
                answerText.innerHTML = `<strong>Answer:</strong> ${qa.a}`;

                questionItem.addEventListener('click', () => {
                    const isVisible = answerText.style.display === 'block';
                    answerText.style.display = isVisible ? 'none' : 'block';
                });

                modalListContainer.appendChild(questionItem);
                modalListContainer.appendChild(answerText);
            });
        }

        modalOverlay.style.display = 'flex';
        document.body.classList.add('modal-open');
    }

    // Function to filter questions
    function filterQuestions(searchQuery, questions) {
        const filteredQuestions = {};
        const query = searchQuery.toLowerCase();

        for (const category in questions) {
            const filtered = questions[category].filter(qa =>
                qa.q.toLowerCase().includes(query) || qa.a.toLowerCase().includes(query)
            );
            if (filtered.length > 0) {
                filteredQuestions[category] = filtered;
            }
        }
        return filteredQuestions;
    }

